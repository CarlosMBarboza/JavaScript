/* Operadores Aritmeticos ( +, -, *, /, **, %, ++, --) */
// Suma
let a = 32, b = 54;
let z = a+b;
console.log("Resultado de la suma: " + z);

// Resta
z = a-b;
console.log("Resultado de la resta: " + z);

// Multiplicacion
z = a*b;
console.log("Resultado de la multiplicacion: " + z);

// Division
z = a/b;
console.log("Resultado de la division: " + z);

// Modulo es el residuo de la division
z = a%b;
console.log("Resultado de la modulo: " + z);

// Exponente
z = a**b;
console.log("Resultado de la exponente: " + z);

//Incremento
//Pre-Incremento se aplica antes de la variable (++  variable )
z = ++a;
console.log("Resultado de la incremento: " + z);

//Post-Incremento se aplica despues de la variable (Variable ++ )
z = b++;
console.log("Resultado de la Pos-incremento: " + z)

//Decremento
//Pre-decremento se aplica antes de la variable ( -- Variable)
z = --a;
console.log("Resultado del Pre-decremento: " +z)

//Pos-decremento se aplica despues de la variable (Variable --)
z = b--;
console.log("Resultado del Pos-decremento: " +z)
